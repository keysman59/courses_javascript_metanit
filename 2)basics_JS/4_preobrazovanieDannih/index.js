//string
console.log("string:")
var number1 = "46";
var number2 = "4";
var result = number1 + number2;
console.log(result);

// parseInt()
console.log("parseInt():")
var number1 = "46";
var number2 = "4";
var result = parseInt(number1) + parseInt(number2);
console.log(result); 

// parseFloat()
console.log("parseFloat():")
var number1 = "46.07";
var number2 = "4.98";
var result = parseFloat(number1) + parseFloat(number2);
console.log(result);

// parseInt()+
console.log("parseInt()+:")
var num1 = "123hello";
var num2 = parseInt(num1);
console.log(num2);

//isNaN()
console.log("isNaN()");
var num1 = "javascript";
var num2 = "22";
var result = isNaN(num1);
console.log(result); // true - num1 не является числом

result = isNaN(num2);
console.log(result); // false - num2 это число




